fun main(args : Array<String>) {

    var num1: Int = 10
    var num2: Double = num1.toDouble()
    println("num1 = $num1")  // integer type
    println("num2 = $num2")  // double type

    var num3: Int = 10
    var num4: Float = num3.toFloat()
    println("num3 = $num3")  // integer type
    println("num4 = $num4")  // float type
}