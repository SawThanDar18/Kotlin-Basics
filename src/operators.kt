fun main(args : Array<String>){

    //operators(+,-,*,%,/)
    var num : Int
    num = 10 + 5  // '+' can replace with '-,*,/,%'
    println("Ans : " + "$num")

    var num1 = 10
    var num2 = 5
    println(num1 + num2) // '+' can replace with '-,*,/,%'

    //operators(increment, decrement)
    var a = 10
    ++a   // pre increment
    println(a)

    var b = 10
    b++   // post increment
    println(b)

    var c = 10
    --c   // pre decrement
    println(c)

    var d = 10
    d--   // post decrement
    println(d)

    var e = 10
    e += 10  // e = e +10 // '+' can replace with '-,*,/,%'
    println(e)
}